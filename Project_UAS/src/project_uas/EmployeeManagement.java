    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String nama = name.getText().trim();
        String posisi = position.getSelectedItem().toString();
        String gajiText = salary.getText().trim();

        // Validasi input
        if (nama.isEmpty() || gajiText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan Gaji tidak boleh kosong.");
            return;
        }

        double gaji;
        try {
            gaji = Double.parseDouble(gajiText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Gaji harus berupa angka.");
            return;
        }

        // Simpan ke database
        try {
            Connection conn = koneksiDB.getConnection();
            String sql = "INSERT INTO karyawan (nama, posisi, gaji) VALUES (?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, nama);
            pst.setString(2, posisi);
            pst.setDouble(3, gaji);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");

            // Bersihkan field setelah simpan
            name.setText("");
            salary.setText("");
            position.setSelectedIndex(0);

            // Refresh tabel
            tampilkanData();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
        }
    }//GEN-LAST:event_saveActionPerformed

