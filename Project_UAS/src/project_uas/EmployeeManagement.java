    private void tampilkanData() {
        DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "NAME", "POSITION", "SALARY"}, 0);
        try {
            Connection conn = koneksiDB.getConnection();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM karyawan");

            while (rs.next()) {
                int id = rs.getInt("id");
                String nama = rs.getString("nama");
                String posisi = rs.getString("posisi");
                double gaji = rs.getDouble("gaji");

                // Masukkan sebagai Double (BUKAN format String) agar bisa diproses lagi
                model.addRow(new Object[]{id, nama, posisi, gaji});
            }
            jTable1.setModel(model);
        } catch (SQLException e) {
            System.out.println("Gagal menampilkan data: " + e.getMessage());
        }
    }

