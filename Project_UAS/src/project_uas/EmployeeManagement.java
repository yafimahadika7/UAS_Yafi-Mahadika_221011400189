    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih data yang ingin diedit.");
            return;
        }

        int id = (int) jTable1.getValueAt(selectedRow, 0);
        String nama = (String) jTable1.getValueAt(selectedRow, 1);
        String posisi = (String) jTable1.getValueAt(selectedRow, 2);
        Object gajiObj = jTable1.getValueAt(selectedRow, 3);

        double gaji;
        if (gajiObj instanceof Double) {
            gaji = (Double) gajiObj;
        } else {
            try {
                gaji = Double.parseDouble(gajiObj.toString().replace(",", "").replace(".", ""));
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Data gaji tidak valid.");
                return;
            }
        }

        EditForm formEdit = new EditForm(id, nama, posisi, gaji);
        formEdit.setVisible(true);
    }//GEN-LAST:event_editActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus.");
            return;
        }

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }

        int id = (int) jTable1.getValueAt(selectedRow, 0); // ambil ID dari kolom pertama

        try {
            Connection conn = koneksiDB.getConnection();
            String sql = "DELETE FROM karyawan WHERE id = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, id);
            pst.executeUpdate();

            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
            tampilkanData(); // refresh tabel
        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
        }
    }//GEN-LAST:event_deleteActionPerformed

